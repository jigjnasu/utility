class BodmasProblems(object):
    def __init__(self, title, file_name):
        self.title = title
        self.file_name = file_name

    def generate_bodmas_problems(self):
        c = canvas.Canvas(self.file_name, pagesize=A4)
        c.setFont("Helvetica-Bold", 13)
        width, height = A4

        for i in range(0, len(QUESTIONS), 3):
            c.setTitle(self.title)
            if i != 0:
                c.showPage()
                c.setFont("Helvetica-Bold", 13)
            c.drawCentredString(270, height-20, self.title)
            y_position = height - 100
            for j in range(3):
                if i + j < len(QUESTIONS):
                    c.drawString(20, y_position, "Find the value of x?")
                    c.drawString(20, y_position - 20, QUESTIONS[i + j])
                    y_position -= 200
                
        c.save()
        print(f"PDF '{self.file_name}' created successfully!!!")


